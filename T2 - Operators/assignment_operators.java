import java.util.*;
public class assignment_operators
{
    public static void main(String args[])
    {
        int A = 10;
        int B = 5;
        int C = 20;
        int D = 30;
        int E = 40;
        //A = A + 10;
        A += 10;
        B -= 5; //B = B - 5;
        C *= 5;
        D /= 3;
        E %= 20;
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);
        System.out.println(D);
        System.out.println(E);
    }
}